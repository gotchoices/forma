    valid_results = [(s, e) for s, e, d, me, v in scan_results if v and e is not None]
    if valid_results:
        sig_min_valid = min(s for s, _ in valid_results)
        sig_max_valid = max(s for s, _ in valid_results)
    else:
        sig_min_valid, sig_max_valid = -0.1, 0.1

    n_sigma_pts = 25
    sig_range = np.linspace(max(sig_min_valid * 0.8, -0.99),
                            min(sig_max_valid * 0.8, 0.99),
                            n_sigma_pts)

