    print(f"  Compare to targets:")
    print(f"    m_n            = {M_N_MEV:.3f} MeV")
    print(f"    m_p + m_e      = {M_P_MEV + M_E_MEV:.3f} MeV")
    print(f"    m_p            = {M_P_MEV:.3f} MeV")
    print(f"    √(m_e² + m_p²) = {E_sum:.3f} MeV")
