# ── Physical constants ────────────────────────────────────────────────

eV_J       = 1.602176634e-19          # J per eV
MeV_J      = eV_J * 1e6              # J per MeV
hbar_c_MeV_fm = hbar * c / MeV_J * 1e15  # ℏc in MeV·fm

M_E_MEV  = m_e * c**2 / MeV_J        # 0.511 MeV
M_P_MEV  = 938.272
M_N_MEV  = 939.565

DM2_21   = 7.53e-5   # eV²
S34      = 0.02199    # neutrino within-plane shear


# ── α formula (Ma convention, from R19 Track 8) ──────────────────────

def alpha_ma(r, s):
    """Fine-structure constant under Ma Compton constraint."""
    mu = math.sqrt(1/r**2 + (2 - s)**2)
    return r**2 * mu * math.sin(2*math.pi*s)**2 / (4*math.pi * (2 - s)**2)


def solve_shear_for_alpha(r, alpha_target=ALPHA):
    """Solve α_Ma(r, s) = α_target for s ∈ (0, 0.5)."""
    s_scan = np.linspace(0.001, 0.49, 3000)
    a_scan = [alpha_ma(r, s) for s in s_scan]
    for i in range(len(s_scan) - 1):
        if (a_scan[i] - alpha_target) * (a_scan[i+1] - alpha_target) < 0:
            return brentq(lambda s: alpha_ma(r, s) - alpha_target,
                          s_scan[i], s_scan[i+1])
    return None


def mu_12(r, s):
    """Dimensionless energy of the (1,2) mode."""
    return math.sqrt(1/r**2 + (2 - s)**2)


# ── Ma metric construction ───────────────────────────────────────────

def build_metric(L, S):
    """
    Build 6×6 Ma metric G = Bᵀ B from circumferences L and shear matrix S.

    Parameters
    ----------
    L : array (6,)   — circumferences [L₁, L₂, L₃, L₄, L₅, L₆]
    S : array (6, 6) — strictly upper-triangular shear matrix

    Returns
    -------
    G : array (6, 6) — metric tensor (symmetric, positive-definite)
    B : array (6, 6) — lattice matrix (upper-triangular)
    """
    B = np.diag(L) @ (np.eye(6) + S)
    G = B.T @ B
    return G, B


def mode_energy_sq(n, Ginv, hbar_c=hbar_c_MeV_fm):
    """
    Squared energy of mode n on Ma.

    E²(n) = (2π ℏc)² nᵀ G⁻¹ n

    Returns energy² in MeV².
    """
    n = np.asarray(n, dtype=float)
    return (2 * math.pi * hbar_c)**2 * n @ Ginv @ n


def mode_energy(n, Ginv, hbar_c=hbar_c_MeV_fm):
    """Energy of mode n in MeV."""
    return math.sqrt(mode_energy_sq(n, Ginv, hbar_c))


def mode_charge(n):
    """
    Net electric charge from tube windings.

    Electron Ma_e: n₁ determines charge sign (n₁ > 0 → −e, n₁ < 0 → +e)
    Proton Ma_p:   n₅ determines charge sign (n₅ > 0 → +e, n₅ < 0 → −e)

    Returns charge in units of e.
    """
    q_e = -n[0]   # electron tube: n₁=+1 → charge −e
    q_p = +n[4]   # proton tube:  n₅=+1 → charge +e
    return q_e + q_p


def mode_spin_label(n):
    """
    Spin label from tube winding parities.

    Odd tube winding (|n₁|, |n₃|, |n₅| odd) → spin-½ contribution.
    Returns a string description.
    """
    tube_parities = [abs(n[0]) % 2, abs(n[2]) % 2, abs(n[4]) % 2]
    n_half = sum(tube_parities)
    if n_half == 0:
        return "boson"
    elif n_half == 1:
        return "fermion (½)"
    elif n_half == 2:
        return "2×½ → 0 or 1"
    else:
        return "3×½ → ½ or 3/2"


# ── Physical scales for each material sheet ──────────────────────────

def compute_scales(r_e, r_nu, r_p):
    """
    Compute the 6 circumferences and 3 within-plane shears.

    Returns (L, s12, s34, s56) where L is array of 6 circumferences in fm.
    """
    s12 = solve_shear_for_alpha(r_e)
    s56 = solve_shear_for_alpha(r_p)
    s34 = S34

    # Electron: E₀_e × μ_e(1,2) = m_e → E₀_e = m_e / μ_e
    mu_e = mu_12(r_e, s12)
    E0_e = M_E_MEV / mu_e               # MeV
    L2 = 2 * math.pi * hbar_c_MeV_fm / E0_e  # fm (ring circumference)
    L1 = r_e * L2                        # fm (tube circumference)

    # Neutrino: E₀_ν determined by Δm²₂₁
    # μ²(1,1) = 1/r² + (1 - s)²,  μ²(-1,1) = 1/r² + (1 + s)²
    # Δm²₂₁ = E₀² [μ²(-1,1) - μ²(1,1)] = E₀² × 4s
    E0_nu_sq = DM2_21 / (4 * s34)       # eV²
    E0_nu = math.sqrt(E0_nu_sq) * 1e-6  # MeV
    L4 = 2 * math.pi * hbar_c_MeV_fm / E0_nu  # fm
    L3 = r_nu * L4

    # Proton: E₀_p × μ_p(1,2) = m_p
    mu_p = mu_12(r_p, s56)
    E0_p = M_P_MEV / mu_p
    L6 = 2 * math.pi * hbar_c_MeV_fm / E0_p
    L5 = r_p * L6

    L = np.array([L1, L2, L3, L4, L5, L6])
    return L, s12, s34, s56


def build_scaled_metric(r_e, r_nu, r_p, sigma_ep=0.0, sigma_enu=0.0, sigma_nup=0.0):
    """
    Build the DIMENSIONLESS Ma metric G̃ and circumferences L.

    The physical metric G has G_ij = L_i L_j G̃_ij where G̃ is O(1).
    This avoids the catastrophic condition number (~10²¹) from the
    fm-to-mm scale hierarchy.

    Mode energy: E²(n) = (2πℏc)² ñᵀ G̃⁻¹ ñ  where ñ_i = n_i/L_i

    Cross-shear convention: G̃_ij = σ for all 4 pairs in a block.
    σ is the cosine of the angle between basis vectors in the
    dimensionless space.

    Returns (Gtilde, Gtilde_inv, L, scales_dict).
    """
    L, s12, s34, s56 = compute_scales(r_e, r_nu, r_p)

    # Build within-plane blocks via B, then scale to G̃ = D⁻¹ G D⁻¹
    S_within = np.zeros((6, 6))
    S_within[0, 1] = s12
    S_within[2, 3] = s34
    S_within[4, 5] = s56
    G_phys, B = build_metric(L, S_within)

    # Scale to dimensionless: G̃_ij = G_ij / (L_i L_j)
    Gtilde = np.zeros((6, 6))
    for i in range(6):
        for j in range(6):
            Gtilde[i, j] = G_phys[i, j] / (L[i] * L[j])

    # Add cross-plane shears (already dimensionless)
    cross_pairs = {
        'enu': [(0, 2), (0, 3), (1, 2), (1, 3)],
        'ep':  [(0, 4), (0, 5), (1, 4), (1, 5)],
        'nup': [(2, 4), (2, 5), (3, 4), (3, 5)],
    }
    sigmas = {'enu': sigma_enu, 'ep': sigma_ep, 'nup': sigma_nup}

    for block, pairs in cross_pairs.items():
        sig = sigmas[block]
        for i, j in pairs:
            Gtilde[i, j] = sig
            Gtilde[j, i] = sig

    Gtilde_inv = np.linalg.inv(Gtilde)

    scales = {
        'L': L, 'L_units': 'fm',
        's12': s12, 's34': s34, 's56': s56,
        'sigma_ep': sigma_ep, 'sigma_enu': sigma_enu, 'sigma_nup': sigma_nup,
        'r_e': r_e, 'r_nu': r_nu, 'r_p': r_p,
    }
    return Gtilde, Gtilde_inv, L, scales


def mode_energy_scaled(n, Gtilde_inv, L, hbar_c=hbar_c_MeV_fm):
    """
    Mode energy using the scaled metric.

    E²(n) = (2πℏc)² ñᵀ G̃⁻¹ ñ   where ñ_i = n_i / L_i
    """
    n = np.asarray(n, dtype=float)
    ntilde = n / L
    return math.sqrt((2 * math.pi * hbar_c)**2 * ntilde @ Gtilde_inv @ ntilde)


