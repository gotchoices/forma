"""
R26 Track 4a: Ma metric and mode spectrum.

Build the 6×6 metric for a flat Ma with shear, compute mode energies,
and verify the block-diagonal (zero cross-shear) limit reproduces
the three independent material sheet spectra from Tracks 1–2.

LATTICE CONVENTION
==================
The Ma lattice is generated by 6 basis vectors in R⁶.  We write the
lattice matrix B (6×6, upper triangular) so that:

    B = L · (I + S)

where L = diag(L₁,...,L₆) gives the circumferences and S is strictly
upper-triangular with shear parameters s_ij (i < j).

The metric tensor is G = Bᵀ B.  The mode energy for quantum numbers
n = (n₁,...,n₆) ∈ Z⁶ is:

    E²(n) = (2π ℏc)² · nᵀ G⁻¹ n

In the zero-cross-shear limit, G is block-diagonal (three 2×2 blocks)
and the spectrum decomposes:  E² = E²_e + E²_ν + E²_p.
"""

